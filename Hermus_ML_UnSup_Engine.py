    # -------------------- Save current clustering model --------------------
    def save_current_model(self):
        """
        Save the current clustering model to its configured trained-model folder.

        This method validates that a current model exists, resolves the appropriate
        save folder from the current model name, creates the folder if necessary,
        builds a timestamped ``.joblib`` filename, and delegates the actual saving
        operation to the model's ``save_model_engine()`` method.

        Returns
        -------
        object or None
            Return value from ``self.current_model.save_model_engine(file_path)``,
            or ``None`` when saving cannot proceed.

        Workflow
        --------
        1. Confirm that a current model exists.
        2. Confirm that the current model name exists.
        3. Confirm that the model supports ``save_model_engine()``.
        4. Resolve the save folder from the model name.
        5. Create the target folder if needed.
        6. Build a timestamped joblib file path.
        7. Save the model through the model's save engine.

        Notes
        -----
        The engine manages save-folder routing centrally so all clustering models
        use a consistent trained-model folder structure.
        """
        logger.info("Saving current clustering model")

        if self.current_model is None:
            logger.warning("save_current_model failed: current_model is None")
            print("⚠️ No current model available ‼️")
            return None

        if self.current_model_name is None:
            logger.warning("save_current_model failed: current_model_name is None")
            print("⚠️ Current model name is missing ‼️")
            return None

        if not hasattr(self.current_model, "save_model_engine"):
            logger.warning("save_current_model failed: save_model_engine not supported")
            print("⚠️ Current model does not support save_model_engine() ‼️")
            return None

        folder_path = self._get_model_save_folder(self.current_model_name)
        if folder_path is None:
            logger.warning(
                "save_current_model failed: unsupported model_name=%s",
                self.current_model_name,
            )
            print("⚠️ Unsupported model type for saving ‼️")
            return None

        os.makedirs(folder_path, exist_ok=True)

        timestamp = time.strftime("%Y%m%d_%H%M%S")
        file_name = f"{self.current_model_name}_{timestamp}.joblib"
        file_path = os.path.join(folder_path, file_name)

        result = self.current_model.save_model_engine(file_path)

        logger.info(
            "Current clustering model saved successfully: model_name=%s, file_path=%s",
            self.current_model_name,
            file_path,
        )
        print(f"🔥 Model saved successfully: {file_path}")
        return result

    # -------------------- Helper: Get saved clustering model folder --------------------
    def _get_model_save_folder(self, model_name: str) -> str | None:
        """
        Return the trained-model save folder path for a given clustering model.

        This internal helper maps a clustering model name to its corresponding
        folder under the project ``ML_Report`` directory.

        Parameters
        ----------
        model_name : str
            Name of the clustering model.

        Returns
        -------
        str or None
            Folder path used for saving trained models of the given type, or
            ``None`` when the model name is unsupported.

        Notes
        -----
        Current folder mappings include:

        - ``KMeans`` -> ``KMean_Trained_Model``
        - ``DBSCAN`` -> ``DBSCAN_Trained_Model``
        - ``AgglomerativeClustering`` -> ``Agglomerative_Trained_Model``
        """
        logger.info("Resolving model save folder for model_name=%s", model_name)

        folder_map = {
            "KMeans": "KMean_Trained_Model",
            "DBSCAN": "DBSCAN_Trained_Model",
            "AgglomerativeClustering": "Agglomerative_Trained_Model",
        }

        folder_name = folder_map.get(model_name)
        if folder_name is None:
            logger.warning(
                "_get_model_save_folder failed: unsupported model_name=%s", model_name
            )
            return None

        folder_path = os.path.join(report_root, folder_name)
        logger.info("Resolved model save folder: %s", folder_path)
        return folder_path

    # -------------------- Helper: Get saved clustering model files --------------------
    def _get_saved_model_files(self, model_name: str) -> list[str]:
        """
        Return saved ``.joblib`` model files for a given clustering model type.

        This internal helper resolves the model-specific trained-model folder,
        searches it for ``.joblib`` files, sorts the files by modification time in
        descending order, and returns the resulting file list.

        Parameters
        ----------
        model_name : str
            Name of the clustering model whose saved files should be listed.

        Returns
        -------
        list[str]
            List of saved model file paths sorted from newest to oldest.

        Notes
        -----
        An empty list is returned when:

        - the model name is unsupported
        - the target save folder does not exist
        - no matching ``.joblib`` files are found
        """
        logger.info("Getting saved model files for model_name=%s", model_name)

        folder_path = self._get_model_save_folder(model_name)
        if folder_path is None:
            logger.warning(
                "_get_saved_model_files failed: unsupported model_name=%s", model_name
            )
            return []

        if not os.path.isdir(folder_path):
            logger.warning("Saved model folder not found: %s", folder_path)
            return []

        model_files = glob.glob(os.path.join(folder_path, "*.joblib"))
        model_files.sort(key=os.path.getmtime, reverse=True)

        logger.info(
            "Found %s saved model file(s) for model_name=%s",
            len(model_files),
            model_name,
        )
        return model_files

    # -------------------- Load saved clustering model --------------------
    def load_trained_model(self, model_name: str, filepath: str):
        """
        Load a trained clustering model from disk and register it as current.

        This method resolves the requested model class from ``MODEL_REGISTRY``,
        creates a model instance using the current clustering feature dataset, and
        restores the saved pipeline by calling the model's
        ``load_model_engine(filepath)`` method. After loading, it registers the
        loaded model as the current active model and synchronizes cluster-related
        runtime state back into the engine.

        Parameters
        ----------
        model_name : str
            Name of the clustering model type to load.
        filepath : str
            File path of the saved ``.joblib`` model file.

        Returns
        -------
        object or None
            Loaded model object if successful; otherwise ``None``.

        Workflow
        --------
        1. Validate that the requested model name exists in ``MODEL_REGISTRY``.
        2. Confirm that clustering input data already exists.
        3. Create a new model instance using the current clustering input data.
        4. Restore the saved trained pipeline through ``load_model_engine()``.
        5. Register the loaded model as the current active model.
        6. Synchronize evaluation, labels, summary, preview, size summary, and
        profile summary back into engine state.

        Notes
        -----
        - This method requires ``self.cluster_input_data`` to already be available,
        because the model load workflow rebuilds clustering runtime state from the
        current input data.
        - The loaded object is not used directly from disk as a standalone engine
        replacement; instead, a fresh model instance is created and then populated
        through its load workflow.
        - Cluster-related cached summaries are expected to be rebuilt by the model
        load workflow before synchronization into Hermes engine state.
        """
        logger.info(
            "Loading trained model: model_name=%s, filepath=%s",
            model_name,
            filepath,
        )

        model_meta = MODEL_REGISTRY.get(model_name)
        if model_meta is None:
            logger.warning(
                "load_trained_model failed: unsupported model_name=%s",
                model_name,
            )
            print(f"⚠️ Unsupported model type: {model_name} ‼️")
            return None

        if self.cluster_input_data is None:
            logger.warning("load_trained_model failed: cluster_input_data is None")
            print("⚠️ Cluster input data is missing. Build X data first ‼️")
            return None

        model_cls = model_meta["class"]

        try:
            loaded_model = model_cls(cleaned_X_data=self.cluster_input_data)

            if not hasattr(loaded_model, "load_model_engine"):
                logger.warning(
                    "load_trained_model failed: load_model_engine not supported for %s",
                    model_name,
                )
                print("⚠️ This model does not support load_model_engine() ‼️")
                return None

            loaded_model.load_model_engine(filepath)

        except Exception as e:
            logger.exception(
                "Failed to load model: model_name=%s, filepath=%s, error=%s",
                model_name,
                filepath,
                e,
            )
            print("⚠️ Failed to load model ‼️")
            return None

        if loaded_model is None:
            logger.warning("load_trained_model failed: loaded_model is None")
            print("⚠️ Loaded model is None ‼️")
            return None

        # ---------- Register loaded model ----------
        self.current_model = loaded_model
        self.current_model_name = model_name
        self.current_model_result = getattr(loaded_model, "evaluation_result", None)
        self.current_cluster_labels = getattr(loaded_model, "labels_", None)
        self.current_cluster_summary = getattr(loaded_model, "cluster_summary", None)
        self.current_cluster_preview = getattr(
            loaded_model, "clustered_data_preview", None
        )
        self.current_cluster_size_summary = getattr(
            loaded_model, "cluster_size_summary", None
        )
        self.current_cluster_profile_summary = getattr(
            loaded_model, "cluster_profile_summary", None
        )

        logger.info(
            "Loaded model runtime state synchronized: summary_ready=%s, preview_ready=%s, size_ready=%s, profile_ready=%s",
            self.current_cluster_summary is not None,
            self.current_cluster_preview is not None,
            self.current_cluster_size_summary is not None,
            self.current_cluster_profile_summary is not None,
        )

        logger.info(
            "Model loaded successfully: model_name=%s, filepath=%s",
            model_name,
            filepath,
        )
        print(f"🔥 Model loaded successfully: {filepath}")
        return loaded_model


